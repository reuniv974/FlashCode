def grey(string):
  return '\033[90m' + string + '\033[0m'

def red(string):
  return '\033[91m' + string + '\033[0m'

def green(string):
  return '\033[92m' + string + '\033[0m'

def yellow(string):
  return '\033[93m' + string + '\033[0m'

def blue(string):
  return '\033[94m' + string + '\033[0m'

def pink(string):
  return '\033[95m' + string + '\033[0m'

def cyan(string):
  return '\033[96m' + string + '\033[0m'